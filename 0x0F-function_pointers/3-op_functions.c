#include "calc.h"
/**
 * op_add - adds two integers
 * @a: 1st int
 * @b: 2nd int
 * Return: sum of two ints
 */
int op_add(int a, int b)
{
	return (a + b);
}
/**
 * op_sub - returns the difference of two ints
 * @a: 1st int
 * @b: 2nd int
 * Return: a - b
 */
int op_sub(int a, int b);
{
	return (a - b);
}
/**
 * op_mul - returns the product of two ints
 * @a: 1st int
 * @b: 2nd int
 * Return: a * b
 */
int op_mul(int a, int b);
{
	return (a * b);
}
/**
 * op_div - returns the results of division of a by b
 * @a: 1st int
 * @b: 2nd int
 * Return: a / b
 */
int op_div(int a, int b);
{
	return (a / b);
}
/**
 * op_mod - returns he remainder of division of a by b
 * @a: 1st int
 * @b: 2nd ints
 * Return: a % b
 */
int op_mod(int a, int b);
{
	return (a % b);
}
